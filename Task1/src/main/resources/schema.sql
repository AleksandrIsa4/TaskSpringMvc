DROP TABLE IF EXISTS users CASCADE;
DROP TABLE IF EXISTS orders CASCADE;

CREATE TABLE IF NOT EXISTS users
(
    user_id   BIGINT GENERATED BY DEFAULT AS IDENTITY,
    first_name CHARACTER VARYING(255) NOT NULL,
    last_name CHARACTER VARYING(255) NOT NULL,
    email CHARACTER VARYING(255) NOT NULL,
    contact_number CHARACTER VARYING(255) NOT NULL,
    CONSTRAINT pk_users PRIMARY KEY (user_id)
);

CREATE TABLE IF NOT EXISTS orders
(
    order_id   BIGINT GENERATED BY DEFAULT AS IDENTITY,
    users_id BIGINT NOT NULL,
    shipping_address CHARACTER VARYING(255) NOT NULL,
    total_price FLOAT NOT NULL,
    products CHARACTER VARYING(255) NOT NULL,
    order_status CHARACTER VARYING(50) NOT NULL,
    CONSTRAINT pk_orders PRIMARY KEY (order_id),
    CONSTRAINT fk_orders_users_id FOREIGN KEY (users_id) REFERENCES users (user_id)
);

